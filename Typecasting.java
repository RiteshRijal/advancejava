public class Typecasting {
    public static void main(String[] args) {
        int a=22;
        int b=7;
        double d=a;
        double e=b;
        double c = d/e;
        System.out.println(c);
        int f=(int)d;
        int g=(int)e;
        System.out.println(f/g);
    }
}
